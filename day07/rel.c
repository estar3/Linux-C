//关系运算符
#include <stdio.h>

int main(void)
{
	printf("1 == 1的结果是%d\n", 1 == 1);
	printf("1 != 1的结果是%d\n", 1 != 1);
	printf("1 > 1的结果是%d\n", 1 > 1);
	printf("1 > 1的结果是%d\n", 1 < 1);
	printf("1 == 1的结果是%d\n", 1 <= 2);
	printf("1 >= 1的结果是%d\n", 1 >= 2);

	int a = 10;
	printf("a == 10的结果是%d\n", a == 10);
	printf("5 < 4 <3的结果是%d\n", 5 < 4 < 3);

	return 0;
}
