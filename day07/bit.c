//位运算符
#include <stdio.h>

int main(void)
{
	//按位与&
	//0x5a:0101 1010
	//0xe3:1110 0011
	//&--------------
	//     0100 0010:0x42
	printf("0x5a & 0xe3的结果是%#x\n", 0x5a & 0xe3);

	//按位或|
	//0x5a:0101 1010
	//0xe3:1110 0011
	//|--------------
	//     1111 1011:0xfb
	printf("0x5a | 0xe3的结果是%#x\n", 0x5a | 0xe3);

	//按位异或^
	//0x5a:0101 1010
	//0xe3:1110 0011
	//^--------------
	//     1011 1001:0xb9
	printf("0x5a ^ 0xe3的结果是%#x\n", 0x5a ^ 0xe3);
	return 0;
}
